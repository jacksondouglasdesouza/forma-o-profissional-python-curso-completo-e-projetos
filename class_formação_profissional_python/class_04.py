print(f'O número de pessoas que participaram da distribuição é de: {numero_pessoas}')
print(f'A quantidade de ovos que sobrão após a distribuição é de: {sobra_ovos}')
print(f'A quantidade de ovos que cada pessoa ganhará será de: {ovos_por_pessoa}')
